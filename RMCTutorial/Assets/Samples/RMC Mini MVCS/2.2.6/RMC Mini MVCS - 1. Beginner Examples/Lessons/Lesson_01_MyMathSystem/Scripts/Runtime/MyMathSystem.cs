
namespace RMC.Mini.Lessons.MyMathSystem
{
    /// <summary>
    /// This performs common math operations on
    /// operands of a and b.
    /// </summary>
    public class MyMathSystem
    {
        public int Add (int a, int b)
        {
            return a + b;
        }

        public int Subtract(int a, int b)
        {
            return a - b;
        }
    }
}